# üé® –£–õ–£–ß–®–ï–ù–ù–ê–Ø –ö–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è Markdown –≤ HTML —Å –ø–æ–¥–¥–µ—Ä–∂–∫–æ–π –∞–±–∑–∞—Ü–µ–≤
def convert_markdown_to_html(text):
    # 1. –°–Ω–∞—á–∞–ª–∞ –¥–µ–ª–∞–µ–º –±–∞–∑–æ–≤—ã–µ –∑–∞–º–µ–Ω—ã –¥–ª—è –∂–∏—Ä–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç–∞, –∫—É—Ä—Å–∏–≤–∞ –∏ –∫–æ–¥–∞
    text = re.sub(r'\*\*(.*?)\*\*', r'<strong>\1</strong>', text)
    text = re.sub(r'\*(.*?)\*', r'<em>\1</em>', text)
    text = re.sub(r'`(.*?)`', r'<code>\1</code>', text)

    # 2. –†–∞–∑–¥–µ–ª—è–µ–º –≤–µ—Å—å —Ç–µ–∫—Å—Ç –Ω–∞ –±–ª–æ–∫–∏ –ø–æ –ø—É—Å—Ç—ã–º —Å—Ç—Ä–æ–∫–∞–º (—ç—Ç–æ –Ω–∞—à–∏ –∞–±–∑–∞—Ü—ã)
    paragraphs = text.split('\n\n')
    html_output = []

    for para in paragraphs:
        # –£–±–∏—Ä–∞–µ–º –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã –ø–æ –∫—Ä–∞—è–º –∞–±–∑–∞—Ü–∞
        para = para.strip()
        if not para:
            continue

        lines = para.split('\n')
        
        # 3. –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –±–ª–æ–∫ —Å–ø–∏—Å–∫–æ–º
        first_line = lines[0].strip()
        is_list = re.match(r'^[‚Ä¢*-] |^\d+\. ', first_line)

        if is_list:
            # –ï—Å–ª–∏ —ç—Ç–æ —Å–ø–∏—Å–æ–∫, –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –∫–∞–∂–¥—É—é —Å—Ç—Ä–æ–∫—É –∫–∞–∫ —ç–ª–µ–º–µ–Ω—Ç —Å–ø–∏—Å–∫–∞
            list_items = []
            for line in lines:
                line = re.sub(r'^[‚Ä¢*-] ', '<span class="bullet">üî∏</span> ', line.strip())
                line = re.sub(r'^(\d+)\. ', r'<strong class="number">\1.</strong> ', line.strip())
                list_items.append(f'<div class="list-item">{line}</div>')
            html_output.append("".join(list_items))
        else:
            # 4. –ï—Å–ª–∏ —ç—Ç–æ –æ–±—ã—á–Ω—ã–π —Ç–µ–∫—Å—Ç (–Ω–∞–ø—Ä–∏–º–µ—Ä, –Ω–∞—à —à–∞–±–ª–æ–Ω),
            # –º—ã —Å–æ–µ–¥–∏–Ω—è–µ–º —Å—Ç—Ä–æ–∫–∏ —Å –ø–æ–º–æ—â—å—é <br> –∏ –æ–±–æ—Ä–∞—á–∏–≤–∞–µ–º –≤—Å—ë –≤ —Ç–µ–≥ <p>
            # –≠—Ç–æ —Å–æ—Ö—Ä–∞–Ω–∏—Ç –≤—Å–µ –ø–µ—Ä–µ–Ω–æ—Å—ã —Å—Ç—Ä–æ–∫ –≤–Ω—É—Ç—Ä–∏ —à–∞–±–ª–æ–Ω–∞.
            html_output.append(f"<p>{'<br>'.join(lines)}</p>")

    # –°–æ–±–∏—Ä–∞–µ–º –≤—Å–µ –æ–±—Ä–∞–±–æ—Ç–∞–Ω–Ω—ã–µ –±–ª–æ–∫–∏ –≤ –µ–¥–∏–Ω—ã–π HTML
    return "".join(html_output)

